t = int(input())
while t:
    d = {4:[2,2],6:[3,3],8:[4,4],10:[5,5],12:[6,6],14:[7,7],16:[8,8]}
    ans = [[8, 8], [7, 7], [8, 6], [3, 1], [1, 3], [5, 7], [8, 4], [5, 1], [1, 5], [3, 7], [8, 2], [7, 1], [1, 7],
           [2, 8], [3, 7], [4, 8], [5, 7], [6, 8]]
    t -= 1
    r, c = map(int, input().split())
    if d.get((r+c),0):
        ans = [[1,1]] + ans
        ans = [d[(r+c)]] + ans
    print(len(ans))
    for i in ans:
        print(i[0],i[1])